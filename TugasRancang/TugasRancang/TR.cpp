void init(void)
{
	glClearColor(0.25, 0.25, 0.25, 1.0);
	glEnable(GL_DEPTH_TEST);
	glDepthFunc(GL_LESS);
	glEnable(GL_NORMALIZE);
	glEnable(GL_COLOR_MATERIAL);
	glMatrixMode(GL_MODELVIEW);

	//pencahayaan
	glLightModeli(GL_LIGHT_MODEL_LOCAL_VIEWER, GL_TRUE);
	glEnable(GL_LIGHTING);
	glEnable(GL_LIGHT0);

	//set cahaya
	GLfloat qaAmbientLight[] = { 0.7,0.7,0.7,1.0 };
	GLfloat qaDiffuseLight[] = { 0.7,0.7,0.7,1 };
	GLfloat qaSpecularLight[] = { 0.7,0.7,0.7,1 };
	GLfloat mat_shininess[] = {50.0f};

	glLightfv(GL_LIGHT0, GL_AMBIENT, qaAmbientLight);
	glLightfv(GL_LIGHT0, GL_DIFFUSE, qaDiffuseLight);
	glLightfv(GL_LIGHT0, GL_SPECULAR, qaSpecularLight);
    glMaterialfv(GL_FRONT, GL_SHININESS, mat_shininess); 

	//posisi cahaya
	GLfloat posisilampu[] = {20,10,50 ,1 };
    GLfloat directedLight[] = {0.7f, 0.7f, 0.7f, 1.0f};

	glLightfv(GL_LIGHT0, GL_POSITION, posisilampu);
	glLightfv(GL_LIGHT0, GL_DIFFUSE, directedLight);
  
	glEnable(GL_COLOR_MATERIAL);
	glShadeModel(GL_SMOOTH);
	glMatrixMode(GL_MODELVIEW);

	glPointSize(9.0);
	glLineWidth(5.0f);
}

void ukuran(int lebar, int tinggi)
{
	if (tinggi == 0) tinggi = 1;

	glMatrixMode(GL_PROJECTION);
	glLoadIdentity();
	gluPerspective(50.0, lebar / tinggi, 5.0, 500.0);
	glTranslatef(0.0, -5.0, -250.0);
	glMatrixMode(GL_MODELVIEW);
}

void idle()
{
	if (!mouseDown)
	{
		xrot += 0.3f;
		yrot += 0.4f;
		glutPostRedisplay();
	}
}

void mouse(int button, int state, int x, int y)
{
	if (button == GLUT_LEFT_BUTTON && state == GLUT_DOWN)
	{
		mouseDown = true;
		xdiff = x - yrot;
		ydiff = -y + xrot;
		glutPostRedisplay();
	}
	else
		mouseDown = false;
}

void mouseMotion(int x, int y)
{
	if (mouseDown)
	{
		yrot = x - xdiff;
		xrot = y + ydiff;
		glutPostRedisplay();
	}
}

void keyboard(unsigned char key, int x, int y)
{
	switch (key)
	{
	case 'w':
	case 'W':
		zmov += 10;
		break;
	case 'd':
	case 'D':
		xmov += 10;
		break;
	case 's':
	case 'S':
		zmov -= 10;
		break;
	case 'a':
	case 'A':
		xmov -= 10;
		break;
	case '7':
		ymov += 10;
		break;
	case '9':
		ymov -= 10;
		break;
	case '2':
		xrot += 10;
		break;
	case '8':
		xrot -= 10;
		break;
	case '6':
		yrot += 10;
		break;
	case '4':
		yrot -= 10;
		break;
	case '1':
		zrot += 10;
		break;
	case '3':
		zrot -= 10;
		break;
	}
	glutPostRedisplay();
}

int main(int argc, char **argv)
{
	glutInit(&argc, argv);
	glutInitDisplayMode(GLUT_DOUBLE | GLUT_RGB);
	glutInitWindowSize(800, 600);
	glutInitWindowPosition(200, 80);
	glutCreateWindow("");
	glutDisplayFunc(display);
	glutReshapeFunc(ukuran);
	glutIdleFunc(display);
	glutMouseFunc(mouse);
	glutMotionFunc(mouseMotion);
	glutKeyboardFunc(keyboard);
	init();
	glutMainLoop();
	return 0;
}